<ul id='answers'>
<li id='q1'>
<div class="questions">
<h5>1.Bagaimana caranya jika tidak mempunyai username dan password ?</h5>
<p>Kamu dapat mengklik tombol signup saat berada di form login, atau kamu dapat menghubungi admin Klinik Pratama Niatha</p>

</div>
</li>

<li id='q2'>
<div class="questions">
<h5>2.Saya sudah mempunyai user untuk login, tapi saya lupa password.</h5>
<p>Kamu dapat mengklik lupa password saat berada di form login, di form lupa password kamu wajib mengisi nomer identitas dan username yang telah didaftarkan.</p>
</div>
</li>

<li id='q3'>
<div class="questions">
<h5>3.Di halaman utama, terdapat nomor antrian. Apakah itu ?</h5>
<p>Nomor antrian yang ada di halaman utama adalah nomor antrian yang pada saat ini sedang diperiksa oleh dokter. Kamu akan mendapatkan nomor antrian jika sudah daftar berobat.</p>
</div>
</li>


<li id='q4'>
<div class="questions">
<h5>4.Bagaimana caranya untuk daftar berobat ?</h5>
<p>Kamu dapat mengklik daftar berobat pada menu, lalu kamu wajib mengisi semua kolom. Jika sudah, kamu akan mednapatkan nomor antrian beserta dengan jam kedatangan</p>
</div>
</li>

<li id='q5'>
<div class="questions">
<h5>5.Apa itu jam kedatangan ?</h5>
<p>Jam kedatangan adalah waktu kamu untuk diperiksa dokter. Kamu wajib datang di waktu jam kedatangan. Jika kamu datang 5 menit lebih dari jam kedatangan, kamu akan didahulukan nomor antrian setelah kamun. Kami sarankan kamu datang lebih awal 5 - 10 menit dari jam kedatangan.</p>
</div>
</li>

<li id='q6'>
<div class="questions">
<h5>6.Apa itu bukti daftar berobat ?</h5>
<p>Menu untuk melihat bukti daftar berobat jika kamu lupa menyimpannya setelah daftar berobat. Bukti ini dapat dilihat pada hari kamu daftar berobat. Jika hari itu kamu belum mendaftar, kamu tidak akan dapat melihat bukti daftar berobat. Contoh, jika kamu membuka website pada tanggal 19 Juli 2017, bukti yang akan keluar adalah bukti daftar berobat tanggal 19 Juli 2017.</p>
</div>
</li>


<li id='q7'>
<div class="questions">
<h5>7. Saya tidak bisa mendaftar untuk saat itu juga.</h5>
<p>Website ini hanya melayani pendaftaran untuk tanggal berikutnya. Jika kamu ingin mendaftar untuk hari ini juga, kamu dapat langsung datang ke Klinik.</p>
</div>
</li>

<li id='q8'>
<div class="questions">
<h5>8. Bagaimana caranya merubah data diri ?</h5>
<p>Kamu dapat mengklik nama kamu di pojok kanan atas.</p>
</div>
</li>

</ul>
